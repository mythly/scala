// Solution-2.scala
// Solution to Exercise 2 in "Conditional Expressions"

val a = 1
val b = 5

if(a < 2) {
  println("a is less than 2")
} else {
  println("a is not less than 2")
}

if(b < 2) {
  println("b is less than 2")
} else {
  println("b is not less than 2")
}

/* OUTPUT_SHOULD_BE
a is less than 2
b is not less than 2
*/
